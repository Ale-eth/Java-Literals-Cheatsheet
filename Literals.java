
// Tambien se conocen como Secuencias de escape o 'Escape sequences' en ingles

public static void main(String[] args) {

    System.out.println ("Hola Mundo. Estoy programando.");        // Original
    // Hola Mundo. Estoy programando.
  
    System.out.println ("Hola Mundo. \nEstoy programando.");      // Salto de linea
    // Hola Mundo.
    // Estoy programando.
  
    System.out.println ("Hola Mundo. \n\tEstoy programando.");    // Un tabulado
    // Hola Mundo.  Estoy programando.
  
    System.out.println ("Hola Mundo. \bEstoy programando.");      // Borra uno
    // Hola Mundo.Estoy programando.
  
    System.out.println ("Hola Mundo. \rEstoy programando.");      // 'Carriage return' (Buscar ref en internet, no lo entendi ðŸ‘€)
    //  Estoy programando.
  
    System.out.println ("Hola Mundo. \'Estoy programando.");      // Inserta una comilla simple
    //  Hola Mundo. 'Estoy programando.
  
    System.out.println ("Hola Mundo. \"Estoy programando.");      // Inserta una comilla doble
    //  Hola Mundo. "Estoy programando.
  
    System.out.println ("Hola Mundo. \\Estoy programando.");      // Inserta una barra invertida
    //  Hola Mundo. \Estoy programando.
  
    
  
    
    
}
